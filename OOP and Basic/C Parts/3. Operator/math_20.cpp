// use of floor() function
#include<iostream>
#include<math.h>

using namespace std;

int main()
{
    double x1 = 2.3;
    cout<<"x1 : "<<x1;

    double result_1 = floor(x1);
    cout<<endl<<"floor("<<x1<<") : "<<result_1<<endl<<endl;
//---------------------------------------------------------------------------------------------------------
    double x2 = -1.5;
    cout<<"x2 : "<<x2;

    double result_2 = floor(x2);
    cout<<endl<<"floor("<<x2<<") : "<<result_2<<endl;
}
