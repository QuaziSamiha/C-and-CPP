// use of ceil() function
#include<iostream>
#include<math.h>

using namespace std;

int main()
{
    double x1 = 2.3;
    cout<<"x1 : "<<x1;

    double result_1 = ceil(x1);
    cout<<endl<<"ceil("<<x1<<") : "<<result_1<<endl<<endl;
//------------------------------------------------------------------------------------------------
    double x2 = -1.8;
    cout<<"x2 : "<<x2;

    double result_2 = ceil(x2);
    cout<<endl<<"ceil("<<x2<<") : "<<result_2<<endl;
}
